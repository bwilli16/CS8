        <h2>Maple Glazed Salmon</h2>
        <figure>
            <img alt="Pic of salmon" src="../images/salmon.jpg">
        </figure>
        <p><b>Ingredients:</b></p>
        <ol>
            <li>6-12oz salmon filet </li>
            <li>1/4 cup maple syrup </li>
            <li>brown rice </li>
            <li>kale </li>
            <li>1/4 cup water </li>
            <li>1tsp olive oil </li>
            <li>salt and pepper to taste</li>
        </ol>
        <p>
            Begin by preparing rice. While it's cooking, preheat oven to 400 degrees. If the salmon filet is large, cut in half or in thirds.
            Line a baking pan with aluminum foil or parchment paper. Place the salmon on the pan
            and coat with half of the maple syrup. Bake for 15 minutes, then briefly remove to 
            re-coat with the rest of the maple syrup. Bake for an additional 5-10 minutes, depending
            on the thickness of the salmon.
        </p>
        <p>
            Chop desired amount of kale if not already done so. In a pan over medium heat, add water,
            oil, and kale. Cover. Remove cover after a few minutes, sautee until kale is soft. Add salt and pepper to tase.
        </p>
         <p>
            Serve salmon and kale over rice.
        </p>
