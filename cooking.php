        <h2>Chicken Fajita</h2>
        <figure>
            <img alt="Pic of fajita" src="../images/fajita.jpg">
        </figure>
        <p><b>Ingredients:</b></p>
        <ol>
            <li>2-3 chicken breasts </li>
            <li>1 bell pepper </li>
            <li>1tbsp olive oil </li>
            <li>taco/fajita seasoning mix </li>
            <li>tortillas </li>
            <li>shredded cheese </li>
            <li>lettuce </li>
        </ol>
        <p>
            Begin by slicing the chicken into small pieces. Place half of the olive oil
            in a pan and heat over medium-high heat. Place the chicken in the pan and sprinkled
            the taco or fajita seasoning over it. Stir occasionally.
        </p>
        <p>
            Cut up a bell pepper and add to the pan after about 5-10 minutes. Add the second half of the
            olive oil. Keep stirring the pan periodically. Cook until chicken is thoroughly cooked, about 20 minutes.
        </p>
         <p>
            Heat up the tortillas in the pan for about a minute per side or microwave for under a minute.
            Add the chicken and peppers to the fajitas, and top with shredded lettuce, cheese, and any additionally
            toppings you may prefer.
        </p>
