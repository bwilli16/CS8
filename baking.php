        <h2>Homemade (or dorm-made) Brownies:</h2>
        <figure>
            <img alt="Pic of food" src="../images/half-dome.jpg">
        </figure>
        <p><b>Ingredients</b></p>
        <ol>
            <li>1 1/4 cups of sugar</li>
            <li>1/4 cups of Water</li>
            <li>2 ounces of unsweetened chocolate</li>
            <li>3/4 cups of all purpose flour </li>
            <li>1/3 cup of unsweetened cocoa powder </li>
            <li>1/2 cups of semi sweet chocolate chips </li>
            <li>1/4 teaspoon of baking powder </li>
            <li>pinch of salt </li>
            <li>2 large eggs </li>
            <li>1/2 cup of vegetable oil </li>
            <li>1 teaspoon of vanilla </li>
        </ol>
        <p>
           Preheat the oven to 350 degrees F. Spray a baking pan with cooking spray and set aside.
        </p>
        <p>
           Combine sugar, water, and unsweetened chocolate in a small sauce pan. Cook over medium low heat
           until the chocolate is melted and appears smooth, but the sugar will not be dissolved (about 3-5 minutes).
           Let the mixture cool for about 5 minutes.
        </p>
        <p>
           In a large bowl mix the flour, cocoa powder, chocolate chips, baking powder, and salt. Pour in the 
           oil, eggs, and vanilla. Pour in the melted chocolate, and mix until everything is combined, but be careful not 
           to over mix!
        </p>
        <p>
           Pour batter into the greased pan and bake for 40-45 minutes. 
        </p>
        <h3>By Erin Brooks. November 29, 2017</h3>
