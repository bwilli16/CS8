        <h2>Chocolate Chip Cookies</h2>
        <figure>
            <img alt="Pic of food" src="../images/half-dome.jpg">
        </figure>
        <p><b>Ingredients:</b></p>
        <ol>
            <li>8 tablespoons of salted butter</li>
            <li>1/2 cup of white sugar</li>
            <li>1/4 cup packed with light brown sugar</li>
            <li>1 teaspoon of vanilla </li>
            <li>1 egg </li>
            <li>1 1/2 cups of all purpose flour </li>
            <li>1/2 teaspoon of baking soda </li>
            <li>pinch of salt </li>
            <li>3/4 cups of chocolate chips </li>
        </ol>
        <p>
            Preheat the oven to 350 degrees F. Microwave the butter for about 40 seconds, just so it is barely melted.
        </p>
        <p>
           Beat the butter and sugar until creamy. Add the vanilla and the egg, and continue beating until combined. 
        </p>
        <p>
           Add the flour, baking soda, and salt, and mix until crumbles form. Use your hands and press the crumbles together into a large ball 
           of dough. Roll the dough into 12 even balls and place on the cookie sheet. Bake for 9-11 minutes, or until the cookies look puffy and dry, but just barely golden.
        </p>
        <h3>By Erin Brooks. November 29, 2017</h3>
