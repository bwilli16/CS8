        <h2>Muffin in a mug</h2>
        <figure>
            <img alt="Chcocolate chip muffin in a mug" src="../Images/muffin.jpg">
        </figure>
        <p>
            The perfect solution for when you want a muffin for dessert, but don't want to make a dozen, and only have a mug and a microwave.
        </p>
        <p><b>Ingredients:</b></p>
        <ol>
            <li>Individual size muffin mix pouch (mine is Duncan Hines chocolate chip muffin)</li>
            <li>3 tablespoons of water</li>
            <li>A mug with a handle</li>
            <li>A spoon</li>
        </ol>
        <p>
            The key to making this recipe is to follow the instructions on the box of muffin mix that you have. I will list what mine tells me to
            do, but yours may be different.
        </p>
        <p>
            Open the pouch and empty the powder into your mug. Measure out 3tbsp (literally 3 spoonfuls) of water and stir it into the mix until the 
            batter is smooth.
        </p>
        <p>
            Microwave on high for however long the box says (this will depend on the wattage of your microwave). Since my microwave has a lower wattage,
            I had to microwave it for slightly longer at 1 min. 50 sec.\
        </p>
        <p>
            Take the mug out of the microwave <strong>using the handle since it won't be as hot</strong> and let it cool for a few minutes before digging
            in with a spoon. Perfect late night snack!
        </p>
        <h3>By Sarah Gaug. December 06, 2017</h3>
